package ChapterSeven;

public class TicTacToe {
    public static void main(String[] args) {
        char[][] ticTacToe = {{'X', 'O', 'O'},{'X', 'X','X','F'},{'O','X','O'}};
        for (int i = 0; i < ticTacToe[i].length; i++){
            System.out.println(ticTacToe[i][i]+ " ");
        }
        System.out.println();
    }
}
